    employed   = code_employment(m02),
    n_domains  = rowSums(
      sapply(MEMBER_ITEMS, function(v) member_binary(elsoc[elsoc$ola==1 & elsoc$muestra==1, v, drop=TRUE])),
      na.rm = TRUE
    )[1:n()]  # align length
  )

# Recompute n_domains safely
mems_w1 <- elsoc %>%
  filter(ola == 1, muestra == 1) %>%
  select(idencuesta, tipo_atricion, all_of(MEMBER_ITEMS)) %>%
  mutate(across(all_of(MEMBER_ITEMS), member_binary)) %>%
  mutate(n_domains = rowSums(across(all_of(MEMBER_ITEMS)), na.rm = TRUE),
         balanced  = as.integer(tipo_atricion == 1))

base_w1 <- elsoc_w1 %>%
  left_join(mems_w1 %>% select(idencuesta, n_domains, balanced),
            by = c("idencuesta", "balanced"))

att_tbl <- base_w1 %>%
  group_by(balanced) %>%
  summarise(
    n            = n(),
    age_mean     = round(mean(age, na.rm = TRUE), 1),
    age_sd       = round(sd(age, na.rm = TRUE), 1),
    pct_woman    = round(mean(woman, na.rm = TRUE) * 100, 1),
    pct_edu_sec  = round(mean(edu_sec, na.rm = TRUE) * 100, 1),
    pct_employed = round(mean(employed, na.rm = TRUE) * 100, 1),
    pct_trust    = round(mean(trust_gen, na.rm = TRUE) * 100, 1),
    pct_trust_nh = round(mean(trust_nh, na.rm = TRUE) * 100, 1),
