load(here::here("data", "ELSOC_Long.RData"))
elsoc <- elsoc_long_2016_2022 %>% as_tibble()

# Balanced panel: muestra==1 & tipo_atricion==1 (waves 1,3,6 → ola 1,2,3)
elsoc_w1 <- elsoc %>%
  filter(ola == 1, muestra == 1) %>%
  mutate(
    balanced   = as.integer(tipo_atricion == 1),
    age        = to_na(m0_edad),
    woman      = as.integer(to_na(m0_sexo) == 2),
    trust_gen  = code_gen_trust(c02),
    trust_nh   = code_nh_trust(t01),
    edu5       = code_education_5(m01),
