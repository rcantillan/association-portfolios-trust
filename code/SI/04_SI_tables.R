# Format as a 4-column table (M1, M2 for trust; M3, M4 for trust_nh)
fmt_ame_cell <- function(est, se, p) {
  sprintf("$%+.3f%s$ \\\\ & $(%.3f)$",
          est, sig_tex(p), se)
}

make_row2 <- function(tbl, clab) {
  row_e <- tbl %>% filter(contrast_label == clab) %>%
    arrange(spec) %>%
    mutate(cell = sprintf("$%+.3f%s$", estimate, sig_tex(p.value))) %>%
    pull(cell)
  row_s <- tbl %>% filter(contrast_label == clab) %>%
    arrange(spec) %>%
    mutate(cell = sprintf("$(%.3f)$", std.error)) %>%
    pull(cell)
  list(
    est = paste0("  ", clab, " & ", paste(row_e, collapse = " & "), " \\\\"),
    se  = paste0("        & ", paste(row_s, collapse = " & "), " \\\\[4pt]")
  )
}

