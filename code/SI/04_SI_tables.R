# Helper: write a section header in the LaTeX output file
tex_section <- function(title) {
  c("", paste0("% ", strrep("=", 60)),
    paste0("% ", title),
    paste0("% ", strrep("=", 60)), "")
}

# Helper: format p-value stars
sig_tex <- function(p) {
  dplyr::case_when(
    p < .001 ~ "^{***}", p < .01 ~ "^{**}", p < .05 ~ "^{*}",
    p < .10  ~ "^{\\dagger}", TRUE ~ ""
  )
}
fmt3 <- function(x) formatC(round(x, 3), format = "f", digits = 3)
fmt1 <- function(x) formatC(round(x * 100, 1), format = "f", digits = 1)

